
#Troca de uma lâmpada queimada
   

#1 Identificar a lâmpada queimada: Verifique qual lâmpada não está acendendo.
#2 Desligar a energia: Desligue o interruptor da lâmpada ou desligue o disjuntor correspondente.
#3 Reunir ferramentas: Pegue uma escada (se necessário) e uma nova lâmpada.
#4 Remover a lâmpada queimada: Gire a lâmpada queimada no sentido anti-horário para soltá-la do soquete.
#5 Instalar a nova lâmpada: Coloque a nova lâmpada no soquete e gire no sentido horário até que esteja firme.
#6 Reativar a energia: Ligue o interruptor ou o disjuntor novamente.
#7 Testar a nova lâmpada: Acenda o interruptor para verificar se a nova lâmpada está funcionando.
#8 Descartar a lâmpada queimada: Descarte a lâmpada queimada de forma adequada, seguindo as normas de reciclagem.     

#Ir para escola
   

#1 Preparar a mochila: Coloque os materiais escolares necessários, como cadernos, livros e canetas.
#2 Vestir-se: Escolha e vista o uniforme ou roupas apropriadas para a escola.
#3 Tomar café da manhã: Faça uma refeição saudável para ter energia durante o dia.
#4 Verificar o horário: Confira o horário de início das aulas para não se atrasar.
#5 Sair de casa: Deixe a casa a tempo, considerando o tempo de deslocamento.
#6 Escolher o meio de transporte: Decida se irá a pé, de bicicleta, de ônibus ou de carro.
#7 Viajar até a escola: Utilize o meio de transporte escolhido para chegar à escola.
#8 Chegar à escola: Entre na escola e dirija-se à sala de aula.

# Preparo de um bolo
   

#1 Reunir os ingredientes: Separe farinha, açúcar, ovos, manteiga, fermento e outros ingredientes necessários.
#2 Preaquecer o forno: Ligue o forno na temperatura adequada (geralmente 180°C) para pré-aquecer.
#3 Misturar os ingredientes secos: Em uma tigela, misture a farinha, o açúcar e o fermento.
#4 Bater os ingredientes molhados: Em outra tigela, bata os ovos e a manteiga até obter uma mistura homogênea.
#5 Combinar as misturas: Adicione a mistura seca à mistura molhada e mexa até ficar homogêneo.
#6 Adicionar sabor (opcional): Incorpore ingredientes adicionais, como chocolate, frutas ou baunilha, se desejado.
#7 Untar a forma: Unte uma forma com manteiga e polvilhe farinha para evitar que o bolo grude.
#8 Despejar a massa: Coloque a massa do bolo na forma untada.
#9 Assar o bolo: Coloque a forma no forno e asse por cerca de 30-40 minutos, ou até que um palito saia limpo.
#10 Deixar esfriar e servir: Retire o bolo do forno, deixe esfriar e desenforme antes de servir.    