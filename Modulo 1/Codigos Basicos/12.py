# Inicializa uma lista vazia
lista = []

# Adiciona elementos à lista
lista.append("Maçã")
lista.append("Banana")
lista.append("Laranja")
lista.append("Uva")
lista.append("Pera")

# Exibe a lista antes da remoção
print("Lista antes da remoção:")
print(lista)

# Remove um elemento da lista
lista.remove("Laranja")

# Exibe a lista após a remoção
print("Lista após a remoção:")
print(lista)
